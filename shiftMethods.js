// Create an array
let fruits = ['apple', 'banana', 'cherry', 'date'];

// Use the shift() method to remove and return the first element
let removedFruit = fruits.shift();

// Display the removed element
console.log('Removed fruit:', removedFruit); // Output: Removed fruit: apple

// Display the modified array after removing the first element
console.log('Modified array:', fruits); // Output: Modified array: ['banana', 'cherry', 'date']
